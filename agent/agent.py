import asyncio
import os
from dotenv import load_dotenv
from livekit.agents import (
    Agent,
    AgentSession,
    JobContext,
    cli,
    WorkerOptions
)
from livekit.plugins import google

# Загружаем переменные окружения
load_dotenv()

VET_SYSTEM_PROMPT = """
Ты — Анна, приветливый и профессиональный ИИ-администратор ветеринарной клиники "Хвостики". 
Твоя задача — консультировать клиентов, записывать их на прием и отвечать на частые вопросы.

ИНФОРМАЦИЯ О КЛИНИКЕ:
- График работы: круглосуточно (24/7).
- Адрес: ул. Пушкина, 10.
- Свободные окна на сегодня: 14:30 (терапевт), 18:00 (хирург), 21:15 (терапевт). На завтра есть много свободного времени с 09:00 до 12:00.
- Цены на популярные услуги: 
  * Первичный прием: 1500 руб.
  * Комплексная вакцинация: 2000 руб.
  * УЗИ: 1800 руб.
  * Стрижка когтей: 500 руб.

ПРАВИЛА ОБЩЕНИЯ (СТРОГО СОБЛЮДАТЬ):
1. ЗНАКОМСТВО: Очень мягко и естественно в начале разговора узнай: как обращаться к клиенту (его имя), какой вид питомца (кошка, собака, птица) и как зовут хвостика. 
2. ЭМПАТИЯ: Будь теплой и заботливой. Если животному плохо — прояви сочувствие ("Ой, бедняжка...", "Как жаль, что Мурзик приболел").
3. ЗАПИСЬ НА ПРИЕМ: Если человек хочет записаться, предложи конкретное время из доступного. Уточни, подходит ли оно.
4. ЭКСТРЕННАЯ СИТУАЦИЯ (КРИТИЧЕСКИ ВАЖНО): Если клиент описывает опасные симптомы (судороги, сильное кровотечение, задыхается, съел яд), немедленно скажи: "Это экстренная ситуация! Пожалуйста, срочно выезжайте к нам, мы работаем круглосуточно. Я прямо сейчас предупрежу врачей, вас примут без очереди!"
5. ФОРМАТ РЕЧИ: Говори кратко, по 1-2 предложения за раз. Представь, что ты говоришь по телефону. Задавай по одному встречному вопросу. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО использовать списки, нумерацию или длинные монологи-лекции.
"""

class VetAssistant(Agent):
    def __init__(self) -> None:
        super().__init__(instructions=VET_SYSTEM_PROMPT)

async def entrypoint(ctx: JobContext):
    await ctx.connect()

    # Тот самый твой рабочий вариант без лишних костылей!
    # Принудительно отключаем Vertex-авторизацию из системы для этого процесса
    os.environ.pop("GOOGLE_APPLICATION_CREDENTIALS", None)

    model = google.realtime.RealtimeModel(
        model="gemini-2.5-flash-native-audio-preview-12-2025",
        voice="Aoede",
        temperature=0.7,
        api_key=os.getenv("GOOGLE_API_KEY")
    )

    session = AgentSession(llm=model)

    await session.start(
        room=ctx.room,
        agent=VetAssistant(),
    )

if __name__ == "__main__":
    cli.run_app(WorkerOptions(entrypoint_fnc=entrypoint))